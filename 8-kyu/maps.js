// Beginner - Lost Without a Map

// Instructions:
// Given an array of integers, return a new array with each value doubled.

// For example:

// [1, 2, 3] --> [2, 4, 6]

// Topics:
// map

// Solution:

function maps(x){
    return x.map(number => number * 2);
}

